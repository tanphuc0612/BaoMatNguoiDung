package Entity;

/**
 *
 * @author admin
 */
public class Role {
    private int roleid;
    private String role;

    public Role(int roleid, String role) {
        this.roleid = roleid;
        this.role = role;
    }

    public int getRoleid() {
        return roleid;
    }

    public void setRoleid(int roleid) {
        this.roleid = roleid;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
    
}
